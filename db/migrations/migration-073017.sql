CREATE TABLE IF NOT EXISTS tops (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255),
  brand VARCHAR(255,
  description VARCHAR,
  image VARCHAR
);

CREATE TABLE IF NOT EXISTS bottoms (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255),
  brand VARCHAR(255,
  description VARCHAR,
  image VARCHAR
);

CREATE TABLE IF NOT EXISTS shoes (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255),
  brand VARCHAR(255,
  description VARCHAR,
  image VARCHAR
);